.PROG
	SETR RINT 63
MAIN:
	LDI R0 10
	LDI R1 11
	LDI R2 12
	LDI R3 13
	CALL CHAMADA:
	JI MAIN:
CHAMADA:
	LDI R1 11
	LDI R2 12
	LD R3 45
	OUT R3 1
	RET
	
.INT0
	IN R3 0
	LDI R1 12
	CALL SALTO:
	ADD R2 R3 R1
	RETI
SALTO:
	SUB R2 R3 R1
	STO R3 45
	RET
	
.INT1
	IN R3 1
	LDI R1 12
	AND R2 R3 R1
	JI SALTO2:
	OR R2 R3 R1
SALTO2:
	NOT R2 R1
	XOR R2 R3 R1
	RETI

.INT2
	IN R3 2
	LDI R1 12
	ADD R2 R3 R1
	STO R3 45
	RETI	
	
.INT3
	IN R3 3
	LDI R1 12
	LDI R2 0
	JZ R2 SALTO3:
	ADD R2 R3 R1
SALTO3:	
	STO R3 45
	NOP
	NOP
	RETI	
	
.INT4
	IN R3 4
	LDI R1 12
	LDI R2 12
	JE R1 R2 SALTO4:
	ADD R2 R3 R1
SALTO4:	
	STO R3 45
	STOP
	RETI	
	
.INT5
	IN R3 5
	LDI R1 12
	ADD R2 R3 R1
	STO R3 45
	RETI	
.END

